#include <unistd.h>

void	ft_putnbr(int nb)
{
	long	nbr;
	char	c;
	int		n;

	n = 1;
	nbr = nb;
	if (nbr < 0)
	{
		write(1, "-", 1);
		nbr = -nbr;
	}
	while (nbr / n > 9)
	{
		n *= 10;
	}
	while (n > 0)
	{
		c = nbr / n + '0';
		write(1, &c, 1);
		nbr %= n;
		n /= 10;
	}
}
